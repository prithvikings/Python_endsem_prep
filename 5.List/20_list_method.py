l1.reverse()#reverse the list
print(l1)

l1.append(45)#add at the end of list
print(l1)

l1.insert(2,245)#insert 245 at index 2
print(l1)

l1.pop(2)#remove element at index 2
print(l1)

l1.remove(21)#remove 21 from the list
print(l1)